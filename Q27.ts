let alienColor: string = "green";

if (alienColor === "green") {
  console.log("You earned 5 points for capturing a green alien.");
} else if (alienColor === "yellow") {
  console.log("You earned 10 points for capturing a yellow alien.");
} else if (alienColor === "red") {
  console.log("You earned 15 points for capturing a red alien.");
} else {
  console.log("Unknown alien color. No points earned.");
}
